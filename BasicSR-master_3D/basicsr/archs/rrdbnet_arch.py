class combo_GAN(nn.Module):
    def __init__(self, combo, cycle):
        super(combo_GAN, self).__init__()
        self.cycle = cycle
        self.combo = combo

    def forward(self,x):
        out1 = self.cycle(x)
        out = self.combo(out1)
        return out
