class combo(nn.Module):
    def __init__(self, rrdb_net, pretrained):
        super(combo, self).__init__()
        self.rrdb = rrdb_net
        self.pretrained = pretrained

    def forward(self,x):
        feat= x
        noise = self.pretrained(feat)
        feat = feat + noise
        out=self.rrdb(feat)
        return out

